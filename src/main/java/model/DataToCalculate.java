package model;





public class DataToCalculate {
    Integer x;
    String name;


    public DataToCalculate(Integer x, String name) {
        this.x = x;
        this.name = name;
    }

    public Integer getX() {
        return x;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
